namespace _03._Var_tredje_Hej
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i <= 30; i++)
            {
                if (i % 3 == 0)
                {
                    Console.WriteLine("Hej");
                }
                else
                {
                    Console.WriteLine(i);
                }
            }
        }
    }
}
